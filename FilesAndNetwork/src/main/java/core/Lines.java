package core;

public class Lines {

    private String number;
    private String name;

    public Lines(String name, String number) {
        this.name = name;
        this.number = number;


    }

    public String getNumber() {
        return number;
    }

    public String getName() {
        return name;
    }

    public String toString() {
        return name;
    }

}

package core;


public class Lines {

    private String number;
    private String name;

    public Lines(String number, String name) {
        this.number = number;
        this.name = name;

    }

    public String getNumber() {
        return number;
    }

    public String getName() {
        return name;
    }

    public String toString()
    {
        return name;
    }
}
